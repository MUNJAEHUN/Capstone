package date_20191216;



// 참거짓을 구분할수없다 switch
public class SwitchBasic {
	public static void main(String[] args) {
		int num=3;
		
		switch(num)
		{
		
		case 1:
			System.out.println("num은 1");
			break;	
		case 2:
			System.out.println("num은 2");
			break;	
		case 3:
			System.out.println("num은 3");
			break;	
		
		default:
			System.out.println("Default value");
			break;	
		}
		
	}// main end
}// class end 
