package date_20191213;


//증감 연산자 
public class OperEx01 {
	public static void main(String[] args) {	
		int c,d;
		c=141;
		d=222;
		int a = 100;
		int b = 100;
		
		a++;
		b--;
		
		++c;
		--d;
		
		System.out.println("a :"+a);
		System.out.println("b :"+b);
		System.out.println("c :"+c);
		System.out.println("d :"+d);
		
	}
}
