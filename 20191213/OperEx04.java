package date_20191213;

//비교 연산자
public class OperEx04 {
	public static void main(String[] args) {
		int a,b;
		a=20;
		b=10;
		
		System.out.println("a > b : "+(a>b));
		System.out.println("a < b : "+(a<b));
		System.out.println("a == b : "+(a==b));
		System.out.println("a <= b : "+(a<=b));
		System.out.println("a >= b : "+(a>=b));
		System.out.println("a != b : "+(a!=b));
	}
}
