package date_20191213;
//산술 연산자 : +,-,*,/,%
public class OperEx02 {
	 public static void main(String[] args) {
	int a=100, b=200;
    
    System.out.println(" a + b = " + a + b);
    
    System.out.println(" a + b = " + (a + b));
    System.out.println(" a - b = " + (a - b));
    System.out.println(" a * b = " + (a * b));
    System.out.println(" a / b = " + (a / b));
    System.out.println(" a % b = " + (a % b));
 }
}
